    // Check the result
    if (memcmp (output, result2, 60) != 0)
    {
        while(1);
    }

    // Test vector 3

    // Expand the key
    CHACHA20_SW_KeyExpand(&context, (uint8_t *)nonce1, (uint8_t *)key0, 32);

    // Perform a 60-byte encryption
